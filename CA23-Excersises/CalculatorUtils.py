def addition(a, b): # This function will create method to add two numbers
    c = a + b
    return int(c)

def subtraction(a, b): # This function will create method to subtract two numbers
    c = a - b
    return int(c)

def multiplication(a, b): # This function will create method to multiply two numbers
    c = a * b
    return int(c)

def division(a, b): # This function will create method to divide two numbers
    c = a / b
    return c


